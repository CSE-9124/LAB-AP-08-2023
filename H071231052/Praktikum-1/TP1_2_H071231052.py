print('Konversi Suhu dari Celcius ke Kelvin, Reamur, dan Farhenheit')
celcius = int(input('Masukkan Suhu dalam Celcius : '))
kelvin = celcius + 273
reamur = (4 / 5) * celcius
farhenheit = (9 / 5) * celcius + 32

print(f'Hasil konversi dari Celcius ke Kelvin adalah : {kelvin}K')
print(f'Hasil konversi dari Celcius ke Reamur adalah : {reamur}R')
print(f'Hasil konversi dari Celcius ke Farhenheit adalah : {farhenheit}F')

